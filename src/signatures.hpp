// XOR key used to encode all sensitive string literals in this file.
// Decoded at first use; never stored as plaintext in the final binary.
static constexpr uint8_t XK = 0x5A;

// Runtime decode: XOR each byte with XK and return as std::string.
inline std::string dx(std::initializer_list<uint8_t> enc) {
    std::string s;
    s.reserve(enc.size());
    for (auto b : enc) s += (char)(b ^ XK);
    return s;
}

